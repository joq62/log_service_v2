-module(log). 
  


%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------


%% --------------------------------------------------------------------

%% External exports


-export([get/1
	]).


%% ====================================================================
%% External functions
%% ====================================================================
get(all)->
    sys:log(log_service,get);
get(error)->
    {ok,Log}=sys:log(log_service,get),
    [{error,Err}||{in,{'$gen_cast',{msg,{error,Err}}}}<-Log];
get(event)->
    {ok,Log}=sys:log(log_service,get),
    [{event,Info}||{in,{'$gen_cast',{msg,{event,Info}}}}<-Log].



